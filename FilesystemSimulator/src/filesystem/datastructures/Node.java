package filesystem.datastructures;

/**
 * Nodo genérico para la lista enlazada manual.
 * @param <T> Tipo de dato almacenado.
 */
public class Node<T> {
    public T data;
    public Node<T> next;

    public Node(T data) {
        this.data = data;
        this.next = null;
    }
}